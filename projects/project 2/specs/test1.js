const assert = require('assert');

describe('Array', () => {
  describe('#indexOf()', () => {
    it('should return -1 when the value is not present', () => {
      assert.equal([1, 2, 3].indexOf(4), -1);
    });
  });

  describe('#indexf()', () => {
    it('1', () => {
      assert.equal([1, 2, 3].indexOf(4), -1);
    });

    it('2', () => {
      assert.equal([1, 2, 3].indexOf(4), -1);
    });

    it('3', () => {
      assert.equal([1, 2, 3].indexOf(4), -1);
    });
  });
});
